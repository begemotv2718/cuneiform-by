uchar stdLowerAscii(uchar c)
{
  if ( c >= (uchar)'A' && c <= (uchar)'Z')
    c = c - (uchar)'A'+ (uchar)'a' ;
  if ( c >= (uchar)'─' && c <= (uchar)'▐')
    c = c - (uchar)'─'+ (uchar)'═' ;
  if ( c >= (uchar)'░' && c <= (uchar)'÷')
    c = c - (uchar)'░'+ (uchar)'Ю' ;
return c;
}
