uchar stdUpperAscii( uchar c )
{
  if ( c >= (uchar)'a' && c <= (uchar)'z')
    c = c - (uchar)'a' + (uchar)'A';

  if ( c >= (uchar)'═' && c <= (uchar)'╞')
    c = c - (uchar)'═' + (uchar)'─';
  if ( c >= (uchar)'Ю' && c <= (uchar)'О')
    c = c - (uchar)'Ю' + (uchar)'░';
return c;
}

